
def addition(num1, num2):
    add = num1 + num2
    return  add

def subtraction(num1, num2):
    sub = num1 - num2
    return  sub

def multiplication(num1, num2):
    mul = num1 * num2
    return  mul

def division(num1, num2):
    div = num1 / num2
    return  div

addition(10 , 5)
subtraction(10, 5)
